func parentChunkIndexes(parent []IpAddress, firstIp, lastIp string) (int, int) {
	var start int
	var stop int
	for i := 0; i < len(parent) && start == 0 || stop == 0; i++ {
		switch {
		case parent[i].address == firstIp:
			start = i
		case parent[i].address == lastIp:
			stop = i
		}
	}
	return start, stop
}

